class operatorsRelational {}

fun main(){

    var value1 = 20
    var value2 = 10

    // == operador de igual
    println(value1 == value2)
    // < operador menor que
    println(value1 < value2)
    // > operador maior que
    println(value1 > value2)
    // <= operador menor ou igual a que
    println(value1 >= value2)
    // >= operador maoior ou igual a que
    println(value1 >= value2)
    // != operador diferente
    println(value1 != value2)
}