class operators {}

fun main(){
    var number1 = 20
    var number2 = 10

    //soma
    println(number1 + number2)
    // subtração
    println(number1 - number2)
    // multiplicação
    println(number1 * number2)
    // divisão
    println(number1 / number2)
    // resto divisão
    println(number1 % number2)
    //operador de incremento
    println(++number1)
    //operador de decremento
    println(--number1)
}